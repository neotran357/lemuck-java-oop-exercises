public class Infinitive_While_Loop {
    // WHILE LOOP dùng khi ta không biết cần phải loop bao nhiêu lần.
    public static void main(String[] args) {
        boolean somethingIsStillTrue = true;

        while (somethingIsStillTrue) {
            System.out.printf("Something Is Still True");

        }
    }
}
